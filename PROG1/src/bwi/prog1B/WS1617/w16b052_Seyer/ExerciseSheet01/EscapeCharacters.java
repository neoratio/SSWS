package bwi.prog1B.WS1617.w16b052_Seyer.ExerciseSheet01;
import bwi.prog.utils.TextIO;
public class EscapeCharacters {

	public static void main(String[] args) {
	
		TextIO.put("escape sequence is started with a backslash: \\\n");
		TextIO.put("to print a tab (\t)	use \"\\t\"\n");
		TextIO.put("to print a backslash (\\)	use \"\\\\\"\n");
		TextIO.put("to print double quotes (\")	use \"\\\"\"\n");
		TextIO.put("to print a single quote (')	use \"\\\'\"\n");
		TextIO.put("to start a new line (\n)				use \"\\n\"\n");
	}

}
